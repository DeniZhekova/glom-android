package com.abborg.glom.model;

public class Category {

    private int id;
    private String name;
    private String iconUrl;
    private boolean isRecommended;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getIconUrl() {
        return iconUrl;
    }

    public boolean isRecommended() {
        return isRecommended;
    }
}
